package com.sip.book_shop.security.dto;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.sip.book_shop.security.constant.AuthConstant;
import com.sip.book_shop.entities.User;
import lombok.NoArgsConstructor;
import org.jspecify.annotations.Nullable;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.Collections;

@NoArgsConstructor
@JsonIgnoreProperties(ignoreUnknown = true, value = {"password", "enabled", "accountNonExpired", "accountNonLocked", "credentialsNonExpired"})
public class CustomUserDetails implements UserDetails {

    private User user;

    public CustomUserDetails(User user) {
        this.user = user;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        String role = user.getRole().getName();
        return Collections.singleton(new SimpleGrantedAuthority(AuthConstant.PREFIX_ROLE + role.toUpperCase()));
    }

    @Override
    public @Nullable String getPassword() {
        return user.getPassword();
    }

    @Override
    public String getUsername() {
        return user.getUsername();
    }

    public String getEmail() {
        return user.getEmail();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
